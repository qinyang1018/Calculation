    /**
     * 档端法
     *
     * @param dangJu 档距
     * @param temp   温度
     * @param h      悬挂点高差
     * @param a      滑车槽至仪器垂直中心距离
     * @return 计算结果
     */
    public String sideOfItemCalculation(Integer dangJu, Integer temp, Double h, Double a) {
        Double f = valueFCalculation(dangJu, temp);
        double sqrt = Math.sqrt(a * f);
        double tanVar = (h - 4 * f + 4 * sqrt) / dangJu;
        double tan = Math.atan(tanVar);
        double v = Math.toDegrees(tan);
        return degreeDetail(v);
    }

    /**
     * 档端法 v2
     *
     * @param dangJu 档距
     * @param temp   温度
     * @param a      滑车槽至仪器垂直中心距离
     * @param du     度数 度
     * @param fen    度数 分
     * @param miao   度数 秒
     * @return
     */
    public String sideOfItemCalculationWithDegrees(Integer dangJu, Integer temp, Double a, Integer du, Integer fen, Integer miao) {
        Double degree = reverseDegreeDetail(du, fen, miao);
        Double f = valueFCalculation(dangJu, temp);
        double h = dangJu * Math.tan(Math.toRadians(degree)) - a;
        double atan = Math.atan((h - 4 * f + 4 * (Math.sqrt(a * f))) / dangJu);
        double v = Math.toDegrees(atan);
        return degreeDetail(v);
    }

    /**
     * 档外
     *
     * @param dangJu 档距
     * @param temp   温度
     * @param l      距就近杆塔中心桩距离
     * @return 计算结果
     */
    public String outSideOfItemCalculation(Integer dangJu, Integer temp, Double l, Integer du1, Integer fen1, Integer miao1, Integer du2, Integer fen2, Integer miao2) {
        Double f = valueFCalculation(dangJu, temp);
        Double degree1 = reverseDegreeDetail(du1, fen1, miao1);
        Double degree2 = reverseDegreeDetail(du2, fen2, miao2);
        double a = l * Math.tan(Math.toRadians(degree2));
        double h = (dangJu + l) * Math.tan(Math.toRadians(degree1)) - a;
        double var1 = h - 4 * f - (8 * l * f / dangJu);
        double B = (2d / dangJu) * var1;
        double var2 = (8 * h * f + 16 * a * f - 16 * Math.pow(f, 2) - Math.pow(h, 2));
        double C = 1d / Math.pow(dangJu, 2) * var2;
        double tanValue = B / 2 + (Math.sqrt(Math.pow(B / 2, 2) + C));
        return degreeDetail(Math.toDegrees(Math.atan(tanValue)));
    }

    private Double valueFCalculation(Integer dangJu, Integer temp) {
        int upDangJu = (dangJu - 200) / 15 * 15 + 200;
        int downDangJu = ((dangJu - 200) / 15) * 15 + 215;
        int leftTemp = (temp + 40) / 10 * 10 - 40;
        int rightTemp = ((temp + 40) / 10) * 10 - 30;
        HashMap<Integer, Double> leftTempMap = dataBase.get(leftTemp);
        Double leftUpDouble = leftTempMap.get(upDangJu);
        Double leftDownDouble = leftTempMap.get(downDangJu);
        HashMap<Integer, Double> rightTempMap = dataBase.get(rightTemp);
        Double rightUpDouble = rightTempMap.get(upDangJu);
        Double rightDownDouble = rightTempMap.get(downDangJu);
        double var2 = (rightDownDouble - rightUpDouble) / 15 * (dangJu - upDangJu) + rightUpDouble;
        double var3 = (leftDownDouble - leftUpDouble) / 15 * (dangJu - upDangJu) + leftUpDouble;
        double var4 = (var2) - (var3);
        return (var4) / 10 * (temp - leftTemp) + var3;
    }

    private static String degreeDetail(double degree) {
        String value = String.valueOf((int) degree) + "°";
        double doubleMin = degree - (int) degree;
        double min = doubleMin * 60;
        value = value + String.valueOf((int) min) + "'";
        double doubleSecond = min - (int) min;
        double second = doubleSecond * 60;
        value = value + String.valueOf(second) + "\"";
        return value;
    }

    private static Double reverseDegreeDetail(Integer du, Integer fen, Integer miao) {
        Double degree;
        degree = (double) du + ((double) fen) / 60 + ((double) miao) / 3600;
        return degree;
    }

